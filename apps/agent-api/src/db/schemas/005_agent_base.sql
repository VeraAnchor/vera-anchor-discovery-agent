-- ============================================================================
-- 8. Add circular reference FKs from actions after dependent tables exist
-- ============================================================================

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'fk_agent_actions_quote_id'
  ) THEN
    ALTER TABLE agent.actions
      ADD CONSTRAINT fk_agent_actions_quote_id
      FOREIGN KEY (quote_id)
      REFERENCES agent.quotes(id)
      ON DELETE SET NULL;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'fk_agent_actions_payment_id'
  ) THEN
    ALTER TABLE agent.actions
      ADD CONSTRAINT fk_agent_actions_payment_id
      FOREIGN KEY (payment_id)
      REFERENCES agent.payment_transactions(id)
      ON DELETE SET NULL;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'fk_agent_actions_receipt_id'
  ) THEN
    ALTER TABLE agent.actions
      ADD CONSTRAINT fk_agent_actions_receipt_id
      FOREIGN KEY (receipt_id)
      REFERENCES agent.receipts(id)
      ON DELETE SET NULL;
  END IF;
END $$;


