CREATE TABLE IF NOT EXISTS agent.quotes (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  action_id UUID REFERENCES agent.actions(id) ON DELETE SET NULL,

  status agent.quote_status_domain NOT NULL DEFAULT 'created',

  quote_kind TEXT NOT NULL DEFAULT 'proof_export',
  provider TEXT NOT NULL DEFAULT 'vera_anchor',

  amount_minor BIGINT NOT NULL,
  currency TEXT NOT NULL,
  network TEXT,

  quote_hash TEXT NOT NULL,
  quote_payload JSONB NOT NULL DEFAULT '{}'::jsonb,

  actor_ref TEXT,
  org_ref TEXT,
  request_id TEXT,
  idempotency_key TEXT,

  expires_at TIMESTAMPTZ NOT NULL,
  accepted_at TIMESTAMPTZ,

  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ,
  deleted_at TIMESTAMPTZ,

  CONSTRAINT agent_quotes_amount_chk CHECK (amount_minor >= 0),
  CONSTRAINT agent_quotes_currency_chk CHECK (currency ~ '^[A-Z0-9_]{2,20}$'),
  CONSTRAINT agent_quotes_provider_chk CHECK (provider ~ '^[a-z][a-z0-9_:-]{1,79}$'),
  CONSTRAINT agent_quotes_kind_chk CHECK (quote_kind ~ '^[a-z][a-z0-9_:-]{1,79}$'),
  CONSTRAINT agent_quotes_hash_chk CHECK (length(quote_hash) BETWEEN 32 AND 256),
  CONSTRAINT agent_quotes_idempotency_key_chk CHECK (
    idempotency_key IS NULL OR length(idempotency_key) BETWEEN 8 AND 256
  )
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_agent_quotes_idempotency_live
  ON agent.quotes (idempotency_key)
  WHERE idempotency_key IS NOT NULL AND deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_quotes_action
  ON agent.quotes (action_id)
  WHERE action_id IS NOT NULL AND deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_quotes_status_expires
  ON agent.quotes (status, expires_at)
  WHERE deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_quotes_actor_created
  ON agent.quotes (actor_ref, created_at DESC)
