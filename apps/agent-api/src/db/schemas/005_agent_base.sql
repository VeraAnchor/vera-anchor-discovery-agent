-- ============================================================================
-- 005_agent_base.sql
-- Version: 1.0
-- Purpose:
--   Base operational ledger for Vera Anchor MCP / discovery agent.
--
-- Notes:
--   - Agent DB does not own Vera Core users/orgs/auth/billing.
--   - actor_ref/org_ref are external references to Core/API/MCP context.
--   - Stores operational receipts, quotes, payment attempts, MCP requests,
--     and public evidence cache records.
-- ============================================================================

CREATE SCHEMA IF NOT EXISTS agent;
CREATE SCHEMA IF NOT EXISTS utils;

-- ============================================================================
-- 1. Audit events
-- ============================================================================

CREATE TABLE IF NOT EXISTS agent.audit_events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

  table_name TEXT NOT NULL,
  record_id UUID,
  record_key TEXT,

  action agent.audit_action_domain NOT NULL,

  old_data JSONB,
  new_data JSONB,

  actor_ref TEXT,
  org_ref TEXT,
  request_id TEXT,
  actor_db_role TEXT NOT NULL DEFAULT current_user,

  changed_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  CONSTRAINT agent_audit_table_name_chk CHECK (
    table_name ~ '^[a-zA-Z_][a-zA-Z0-9_]*\.[a-zA-Z_][a-zA-Z0-9_]*$'
  )
);

CREATE INDEX IF NOT EXISTS idx_agent_audit_table_changed_at
  ON agent.audit_events (table_name, changed_at DESC);

CREATE INDEX IF NOT EXISTS idx_agent_audit_record_id
  ON agent.audit_events (table_name, record_id)
  WHERE record_id IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_agent_audit_record_key
  ON agent.audit_events (table_name, record_key)
  WHERE record_key IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_agent_audit_actor_changed_at
  ON agent.audit_events (actor_ref, changed_at DESC)
  WHERE actor_ref IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_agent_audit_org_changed_at
  ON agent.audit_events (org_ref, changed_at DESC)
  WHERE org_ref IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_agent_audit_request_id
  ON agent.audit_events (request_id)
  WHERE request_id IS NOT NULL;


-- ============================================================================
-- 2. Actions
-- ============================================================================

CREATE TABLE IF NOT EXISTS agent.actions (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  action_type agent.action_type_domain NOT NULL,
  status agent.action_status_domain NOT NULL DEFAULT 'created',

  actor_ref TEXT,
  org_ref TEXT,
  request_id TEXT,
  idempotency_key TEXT,

  quote_id UUID,
  payment_id UUID,
  receipt_id UUID,

  input_hash TEXT,
  params_hash TEXT,
  output_hash TEXT,

  core_reference_id TEXT,
  core_reference_type TEXT,

  attempts INTEGER NOT NULL DEFAULT 0,
  max_attempts INTEGER NOT NULL DEFAULT 3,

  error_code TEXT,
  error_message TEXT,

  metadata JSONB NOT NULL DEFAULT '{}'::jsonb,

  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ,
  started_at TIMESTAMPTZ,
  completed_at TIMESTAMPTZ,
  expires_at TIMESTAMPTZ,
  deleted_at TIMESTAMPTZ,

  CONSTRAINT agent_actions_attempts_chk CHECK (attempts >= 0),
  CONSTRAINT agent_actions_max_attempts_chk CHECK (max_attempts BETWEEN 0 AND 25),

  CONSTRAINT agent_actions_input_hash_chk CHECK (
    input_hash IS NULL OR length(input_hash) BETWEEN 32 AND 256
  ),
  CONSTRAINT agent_actions_params_hash_chk CHECK (
    params_hash IS NULL OR length(params_hash) BETWEEN 32 AND 256
  ),
  CONSTRAINT agent_actions_output_hash_chk CHECK (
    output_hash IS NULL OR length(output_hash) BETWEEN 32 AND 256
  ),
  CONSTRAINT agent_actions_idempotency_key_chk CHECK (
    idempotency_key IS NULL OR length(idempotency_key) BETWEEN 8 AND 256
  )
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_agent_actions_idempotency_live
  ON agent.actions (idempotency_key)
  WHERE idempotency_key IS NOT NULL AND deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_actions_status_created
  ON agent.actions (status, created_at DESC)
  WHERE deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_actions_actor_created
  ON agent.actions (actor_ref, created_at DESC)
  WHERE actor_ref IS NOT NULL AND deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_actions_org_created
  ON agent.actions (org_ref, created_at DESC)
  WHERE org_ref IS NOT NULL AND deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_actions_request_id
  ON agent.actions (request_id)
  WHERE request_id IS NOT NULL AND deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_actions_core_reference
  ON agent.actions (core_reference_type, core_reference_id)
  WHERE core_reference_id IS NOT NULL AND deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_agent_actions_expiring
  ON agent.actions (expires_at)
  WHERE expires_at IS NOT NULL AND deleted_at IS NULL;


-- ============================================================================
-- 3. Quotes
